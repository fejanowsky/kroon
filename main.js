function changeLanguage(lang) {
    const t = translations[lang];
    
    // Main content
    document.getElementById('main-title').textContent = t.title;
    document.getElementById('main-description').textContent = t.description;
    document.getElementById('contact-button').textContent = t.contactButton;
    document.getElementById('learn-button').textContent = t.learnButton;
    
    // Update selected language in dropdown
    const languageMap = {
        'pt': '🇧🇷 PT',
        'en': '🇺🇸 EN',
        'cn': '🇨🇳 CN',
        'es': '🇪🇸 ES',
        'jp': '🇯🇵 JP',
        'elf': '✧ ELF'
    };
    const selectedLang = document.querySelector('.language-dropdown > div:first-child');
    selectedLang.innerHTML = `${languageMap[lang]} <i class="fas fa-chevron-down" style="margin-left: 5px"></i>`;
    
    // Contact modal updates
    document.getElementById('contact-title').textContent = t.contactTitle;
    const contactModal = document.getElementById('contact-us');
    const whatsappText = contactModal.querySelector('div:first-of-type p:first-of-type');
    const emailText = contactModal.querySelector('div:last-of-type p:first-of-type');
    whatsappText.textContent = t.contactInfo.whatsapp;
    emailText.textContent = t.contactInfo.email;
    const humorousNote = contactModal.querySelector('p[style*="font-style: italic"]');
    if (humorousNote) {
        humorousNote.textContent = t.contactInfo.humorousNote;
    }
    
    // Learn More modal updates
    document.getElementById('learn-more-title').textContent = t.learnMoreTitle;
    
    // Update main content paragraphs
    const mainParagraphs = ['p1', 'p2', 'p4', 'p5', 'p6'];
    mainParagraphs.forEach(key => {
        const elem = document.getElementById(`learn-more-${key}`);
        if (elem && t.about[key]) {
            elem.textContent = t.about[key];
        }
    });
    
    // Update client list with formatting
    if (t.about.clientsIntro && t.about.clientList) {
        const clientHtml = t.about.clientsIntro + ' ' + 
            t.about.clientList.map(client => 
                `<b style="color: #00c28b">${client}</b>`
            ).join(', ');
        document.getElementById('learn-more-p7').innerHTML = clientHtml;
    }
    
    // Update comedy section
    if (t.about.comedyIntro) {
        const comedyP = document.querySelector('#learn-more p:nth-of-type(8)');
        if (comedyP) {
            comedyP.innerHTML = `${t.about.comedyIntro} <b style="color: #00c28b">A Culpa é do Cabral</b>, <b style="color: #00c28b">República do Stand Up</b>`;
        }
    }
    
    // Update history section
    if (t.about.historyIntro) {
        const historyP = document.querySelector('#learn-more p:nth-of-type(9)');
        if (historyP) {
            historyP.innerHTML = `${t.about.historyIntro} <b style="color: #00c28b">Meus Prêmios Nick</b>`;
        }
    }
    
    // Update commitment text
    const commitmentP = document.querySelector('#learn-more p[style*="text-align: center"]');
    if (commitmentP && t.about.commitment) {
        commitmentP.textContent = t.about.commitment;
    }
    
    // Close buttons
    const closeButtons = document.querySelectorAll('button[aria-label="Fechar modal"]');
    closeButtons.forEach(button => {
        button.setAttribute('aria-label', t.closeButton);
    });
}

